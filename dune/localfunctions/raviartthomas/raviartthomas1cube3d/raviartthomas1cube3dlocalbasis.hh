      out[12][1] = 0.0;
      out[12][2] = 0.0;

      out[13][0] = -6.0*in[0] + 12.0*in[0]*in[2] + 9.0*in[0]*in[0] - 18.0*in[0]*in[0]*in[2];
      out[13][1] = 0.0;
      out[13][2] = 0.0;

      out[14][0] = 0.0;
      out[14][1] = 3.0 - 12.0*in[1] - 6.0*in[2] + 24.0*in[1]*in[2] + 9.0*in[1]*in[1] - 18.0*in[1]*in[1]*in[2];
      out[14][2] = 0.0;

