      out[0][2] = 0.0;

      out[1][0] = sign1*(-2.0*in[0] + 3.0*in[0]*in[0]);
      out[1][1] = 0.0;
      out[1][2] = 0.0;

      out[2][0] = 0.0;
      out[2][1] = sign2*(-3.0*in[1]*in[1] + 4.0*in[1] - 1.0);
      out[2][2] = 0.0;

      out[3][0] = 0.0;
      out[3][1] = sign3*(3.0*in[1]*in[1] - 2.0*in[1]);
      out[3][2] = 0.0;

      out[4][0] = 0.0;
      out[4][1] = 0.0;
      out[4][2] = sign4*(-3.0*in[2]*in[2] + 4.0*in[2] - 1.0);

      out[5][0] = 0.0;
      out[5][1] = 0.0;
      out[5][2] = sign5*(3.0*in[2]*in[2] - 2.0*in[2]);

